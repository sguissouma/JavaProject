package model;

/**
 * @author Carlos Villavicencio
 *
 */
public class Coord2D {
	public int x;
	public int y;

	public Coord2D(int x, int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return this.x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return this.y;
	}

	public void setY(int y) {
		this.y = y;
	}

}